let Allmap = {};
let AMK = [];

function getList(arg) {
  switch (arg) {
    case 'us': return EN;
    case 'gq': return FG;
    case 'quan': return QC;
    default: return ZH;
  }
}

function initMap() {
  if (AMK.length) return;
  const outList = getList(outputName);
  const inputList = inname ? [getList(inname)] : [ZH, FG, QC, EN];

  inputList.forEach(arr => {
    arr.forEach((v, i) => Allmap[v] = outList[i]);
  });
  AMK = Object.entries(Allmap);
}

function operator(proxies) {
  initMap();
  const BLKEYS = BLKEY ? BLKEY.split("+") : [];

  // 过滤
  if (clear || nx || blnx || key) {
    proxies = proxies.filter(p => {
      const n = p.name;
      if (clear && nameclear.test(n)) return false;
      if (nx && namenx.test(n)) return false;
      if (blnx && !nameblnx.test(n)) return false;
      return true;
    });
  }

  proxies.forEach(proxy => {
    let retainKey = "";
    let ikey = "", ikeys = "";
    const origName = proxy.name;

    // rurekey 替换
    Object.keys(rurekey).forEach(k => {
      if (rurekey[k].test(proxy.name)) {
        proxy.name = proxy.name.replace(rurekey[k], k);
      }
    });

    // block-quic
    if (blockquic === "on") proxy["block-quic"] = "on";
    else if (blockquic === "off") proxy["block-quic"] = "off";
    else delete proxy["block-quic"];

    // BLKEY
    if (BLKEY) {
      BLKEYS.forEach(item => {
        if (item.includes(">")) {
          const [from, to] = item.split(">");
          if (origName.includes(from)) retainKey = to || from;
        } else if (origName.includes(item)) {
          retainKey = item;
        }
      });
    }

    // 倍率
    if (blgd) {
      regexArray.forEach((reg, i) => {
        if (reg.test(proxy.name)) ikeys = valueArray[i];
      });
    }
    if (bl) {
      const m = proxy.name.match(/(\d+\.?\d*)\s*[×xX倍]/);
      if (m && m[1] !== "1") ikey = m[1] + "×";
    }

    // 匹配国家
    const match = AMK.find(([k]) => proxy.name.includes(k) || origName.includes(k));

    if (match) {
      const country = match[1];
      let flag = "";

      if (addflag) {
        const idx = ZH.indexOf(country);
        flag = idx !== -1 ? FG[idx] : "";
        if (flag === "🇹🇼") flag = "🇨🇳";
      }

      const parts = [];
      if (nf) parts.push(FNAME);
      if (flag) parts.push(flag);
      if (!nf) parts.push(FNAME);
      parts.push(country, retainKey, ikey, ikeys);

      proxy.name = parts.filter(Boolean).join(FGF);
    } else {
      proxy.name = nm ? (FNAME + FGF + origName) : null;
    }
  });

  return proxies.filter(p => p.name !== null);
}

// ============ 必须保留这一行 ============
